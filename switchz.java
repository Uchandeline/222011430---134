class switchz {
    public static void main(String args[]){ 
        int number=8;
        switch (number){
            case 6: System.out.println("value is 6");
            break;
            case 7: System.out.println("value is 7");
            break;
            case 8: System.out.println("value is 8");
            break;
            case 9: System.out.println("value is 9");
            break;
            default:System.out.println("out of range ");
        }
    }}